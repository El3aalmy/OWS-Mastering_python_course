# ---- Data types --------------------------
# All data in python is object 
# type() => return the type of data in (  )
# -------------------------------------------

print(type(10)) # Int => integer  
print(type(100)) # Int => integer  
print(type(-40)) # Int => integer  

print(type(2.6)) # float => floating point number 
print(type(100.47)) # float => floating point number 
print(type(-99.55)) # float => floating point number 

print(type("hasan")) # str => string 

print(type([1, 2, 3, 4, 5])) # list => list 

print(type((1, 2, 3, 4, 5))) # tuple => tuple 

print(type( {"One" : 1, "Two" : 2, "Three" : 3} )) # dict => dictionary 

print(type(2 == 2)) # bool => boolean 
